package operadoresLogicos;

public class Atividade4 {

	public static void main(String[] args) {

	float n1 = 5.0f;
	float n2 = 6.0f;
	float n3 = 7.0f;
	float n4 = 8.0f;
		
	float diferenca1 = (n1 * n2) + (n3 * n4);
	System.out.println("A diferença 1 é de: " + diferenca1);
		
	float diferenca2 = (n1 * n2) + (-n3 * n4);
	System.out.println("A diferença 2 é de: " + diferenca2);
		
	}

}